    private void jtfDescontoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfDescontoKeyTyped
        if ((evt.getKeyChar() + "").equals(".")) {
            ponto++;
            if (ponto > 1) {
                evt.consume();
            }
        }
    }//GEN-LAST:event_jtfDescontoKeyTyped

